import * as React from 'react'
import Head from 'next/head'
import styles from '@/styles/Home.module.css'
import JobPostForm from '@/components/JobPostForm'
import { useRouter } from 'next/router'
import JobData from '@/interfaces/JobData'

type serverProps = {
  params: {
    id: number
  }
}

export async function getServerSideProps({ params }: serverProps) {
  return {
      props: { id: params.id }
  }
}

export default function JobEditPage() {
  const [uid, setUid] = React.useState<string | undefined>(undefined)
  const router = useRouter()
  const { id } = router.query
  React.useEffect(() => {
    setUid(id as string)
  }, [id])

  const save = (data: JobData) => {
    fetch('/api/job-posts/save', {
      method: 'PUT',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(data),
    }).then(res => {
      console.log(res.json())
    })
    router.push('/')
  }
  return (
    <>
      <Head>
        <title>Job Post App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={`${styles.main}`}>
        {
          uid && <JobPostForm uid={uid} onSave={save}/>
        }
      </main>
    </>
  )
}
